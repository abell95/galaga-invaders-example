Reflecting on Principle
1) How are Ruby modules (and similar features from other langs)
   different from a class, in their purpose?

   Modules are reusable functionality that can be implemented by a class without the weight of instantiating a whole class.

2) What is LSP?

    Liskov Substitution Principle states that for a given type, all its subtypes should be able to stand in wherever
    that type is expected.

3) Briefly describe how the template method pattern works.

    A superclass handles the early steps of the algorithm or provides a base for subclasses to override and implement
    their more specific requirements for the method.

